public class Porta {
    private boolean aberta = false;
    private String cor;
    private double dX;
    private double dY;
    private double dZ;

    public boolean isAberta() {
        return aberta;
    }

    public void setAberta(boolean aberta) {
        this.aberta = aberta;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public double getdX() {
        return dX;
    }

    public void setdX(double dX) {
        this.dX = dX;
    }

    public double getdY() {
        return dY;
    }

    public void setdY(double dY) {
        this.dY = dY;
    }

    public double getdZ() {
        return dZ;
    }

    public void setdZ(double dZ) {
        this.dZ = dZ;
    }
}
